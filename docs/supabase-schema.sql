-- Supabase schema + RLS for per-user candidates/resumes

create table if not exists public.jobs (
  id uuid primary key default gen_random_uuid(),
  user_id uuid not null references auth.users(id) on delete cascade,
  title text not null,
  description text,
  icon text,
  department text,
  location text,
  type text,
  openings integer not null default 1 check (openings >= 1),
  status text not null default 'active' check (status in ('active', 'inactive')),
  created_at timestamptz not null default now()
);

create table if not exists public.candidates (
  id uuid primary key default gen_random_uuid(),
  user_id uuid not null references auth.users(id) on delete cascade,
  full_name text not null,
  job_title text not null,
  stage text not null default 'Applied',
  email text,
  location text,
  years_experience numeric,
  skills text[] not null default '{}',
  education text[] not null default '{}',
  resume_path text,
  resume_preview_url text,
  ats_score numeric,
  analysis_summary text,
  analysis_strengths text[] not null default '{}',
  analysis_concerns text[] not null default '{}',
  ai_writing_score numeric,
  ai_writing_label text,
  ai_writing_notes text[] not null default '{}',
  interview_questions text[] not null default '{}',
  offer_summary text,
  offer_checklist jsonb not null default '{"interviewCompleted":false,"referencesChecked":false,"termsAligned":false,"internalApproval":false,"offerSent":false}'::jsonb,
  offer_outcome text not null default 'pending',
  offer_sent_at date,
  offer_response_due_at date,
  skill_profile jsonb,
  analysis_status text not null default 'pending_ai',
  interview_analysis_status text not null default 'none',
  interview_analysis_score numeric,
  interview_analysis_summary text,
  interview_analysis_strengths text[] not null default '{}',
  interview_analysis_concerns text[] not null default '{}',
  interview_analysis_questions text[] not null default '{}',
  interview_analysis_transcript_ids uuid[] not null default '{}',
  interview_analysis_updated_at timestamptz,
  created_at timestamptz not null default now()
);

alter table public.candidates
  add column if not exists interview_analysis_status text not null default 'none',
  add column if not exists interview_analysis_score numeric,
  add column if not exists interview_analysis_summary text,
  add column if not exists interview_analysis_strengths text[] not null default '{}',
  add column if not exists interview_analysis_concerns text[] not null default '{}',
  add column if not exists interview_analysis_questions text[] not null default '{}',
  add column if not exists interview_analysis_transcript_ids uuid[] not null default '{}',
  add column if not exists interview_analysis_updated_at timestamptz;

create table if not exists public.signup_rate_limits (
  id bigint generated by default as identity primary key,
  ip text not null,
  attempts int not null default 0,
  window_start timestamptz not null default now(),
  created_at timestamptz not null default now()
);

create table if not exists public.ai_agent_settings (
  id uuid primary key default gen_random_uuid(),
  user_id uuid not null references auth.users(id) on delete cascade,
  job_id uuid not null references public.jobs(id) on delete cascade,
  scoring_strictness text not null default 'balanced',
  response_tone text not null default 'professional',
  interview_question_style text not null default 'practical',
  ai_writing_sensitivity text not null default 'balanced',
  evaluation_focus text[] not null default '{}',
  custom_instructions text,
  updated_at timestamptz not null default now(),
  unique (user_id, job_id)
);

create table if not exists public.offer_documents (
  id uuid primary key default gen_random_uuid(),
  user_id uuid not null references auth.users(id) on delete cascade,
  candidate_id uuid not null references public.candidates(id) on delete cascade,
  title text not null,
  content text not null,
  inputs jsonb not null default '{}'::jsonb,
  status text not null default 'draft',
  generated_by uuid references auth.users(id) on delete set null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.activity_logs (
  id uuid primary key default gen_random_uuid(),
  user_id uuid not null references auth.users(id) on delete cascade,
  action text not null,
  entity_type text not null check (entity_type in ('candidate', 'job', 'offer_document')),
  entity_id uuid,
  entity_label text,
  from_value text,
  to_value text,
  metadata jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now()
);

create table if not exists public.interview_studio_boards (
  id uuid primary key default gen_random_uuid(),
  user_id uuid not null references auth.users(id) on delete cascade,
  title text not null default 'Studio razgovorov',
  nodes jsonb not null default '[]'::jsonb,
  edges jsonb not null default '[]'::jsonb,
  transcripts jsonb not null default '[]'::jsonb,
  viewport jsonb not null default '{"x":-220,"y":-110,"zoom":1}'::jsonb,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  unique (user_id)
);

create table if not exists public.interview_transcripts (
  id uuid primary key default gen_random_uuid(),
  user_id uuid not null references auth.users(id) on delete cascade,
  title text not null,
  transcript_text text,
  audio_path text,
  audio_mime_type text,
  duration_seconds integer not null default 0 check (duration_seconds >= 0 and duration_seconds <= 3600),
  status text not null default 'recorded' check (status in ('recorded', 'processing', 'complete', 'failed')),
  source text not null default 'recording' check (source in ('recording', 'manual')),
  estimated_cost_usd numeric,
  actual_cost_usd numeric,
  error_message text,
  metadata jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.candidate_interview_transcripts (
  id uuid primary key default gen_random_uuid(),
  user_id uuid not null references auth.users(id) on delete cascade,
  candidate_id uuid not null references public.candidates(id) on delete cascade,
  transcript_id uuid not null references public.interview_transcripts(id) on delete cascade,
  linked_at timestamptz not null default now(),
  unique (user_id, candidate_id, transcript_id),
  unique (user_id, transcript_id)
);

