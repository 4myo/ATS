  for select using (user_id = auth.uid());
create policy "candidate_interview_transcripts_insert_own" on public.candidate_interview_transcripts
  for insert with check (
    user_id = auth.uid()
    and exists (
      select 1 from public.candidates
      where candidates.id = candidate_interview_transcripts.candidate_id
        and candidates.user_id = auth.uid()
    )
    and exists (
      select 1 from public.interview_transcripts
      where interview_transcripts.id = candidate_interview_transcripts.transcript_id
        and interview_transcripts.user_id = auth.uid()
    )
  );
create policy "candidate_interview_transcripts_update_own" on public.candidate_interview_transcripts
  for update using (user_id = auth.uid())
  with check (
    user_id = auth.uid()
    and exists (
      select 1 from public.candidates
      where candidates.id = candidate_interview_transcripts.candidate_id
        and candidates.user_id = auth.uid()
    )
    and exists (
      select 1 from public.interview_transcripts
      where interview_transcripts.id = candidate_interview_transcripts.transcript_id
        and interview_transcripts.user_id = auth.uid()
    )
  );
create policy "candidate_interview_transcripts_delete_own" on public.candidate_interview_transcripts
  for delete using (user_id = auth.uid());

create index if not exists candidate_interview_transcripts_candidate_idx
  on public.candidate_interview_transcripts (user_id, candidate_id, linked_at desc);
create index if not exists candidate_interview_transcripts_transcript_idx
  on public.candidate_interview_transcripts (user_id, transcript_id);
create unique index if not exists candidate_interview_transcripts_one_candidate_per_transcript_idx
  on public.candidate_interview_transcripts (user_id, transcript_id);

